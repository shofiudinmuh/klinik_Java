    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        String sql = "insert into kamar values (?,?,?,?,?)";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtkode.getText());
            stat.setString(2, jenisKamar.getSelectedItem().toString());
            stat.setString(3, txtnama.getText());
            stat.setString(4, txttarif.getText());
            stat.setString(5, txtfasilitas.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "data berhasil disimpan");
            kosong();
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "data gagal disimpan"+e);
        }
        datatable();
    
        
    }//GEN-LAST:event_tambahActionPerformed

