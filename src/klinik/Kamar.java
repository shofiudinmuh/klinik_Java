    protected void autonumber(){
        try{
            String sql = "SELECT kode_kamar FROM kamar order by kode_kamar asc";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            txtkode.setText("K0001");
            while(rs.next()){
                String id = rs.getString("kode_kamar").substring(2);
                int AN = Integer.parseInt(id)+1;
                String Nol ="";
                
                if(AN<10){
                    Nol = "000";
                }
                else if (AN<100){
                    Nol = "00";
                }
                else if (AN<1000){
                    Nol = "0";
                }
                else if(AN<10000){
                    Nol = "";
                }
                txtkode.setText("K"+Nol+AN);
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Auto number gagal"+e);
        }
    }
    protected void datatable(){
        Object[]Baris = { "Kode Kamar", "Jenis Kamar", "Nama Kamar", "Tarif", "Fasilitas"};
        tabmode = new DefaultTableModel(null, Baris);
        String cariitem = txtcariitem.getText();
        
        try{
            String sql = "SELECT * FROM kamar where Jenis_Kamar like '%"+cariitem+"%' or Nama_Kamar like '%"+cariitem+"%' order by kode_kamar asc";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                tabmode.addRow(new Object[]{
                hasil.getString(1),
                hasil.getString(2),
                hasil.getString(3),
                hasil.getString(4),
                hasil.getString(5)
                        });
            }
            TabelKamar.setModel(tabmode);
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null,"data gagal dipanggil"+e);
            }
            
        }
    

